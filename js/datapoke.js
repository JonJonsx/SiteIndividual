class Pokemon {
    constructor(
        id,
        name,
        image,
        height,
        weight,
        hp,
        atk,
        def,
        spAtk,
        spDef,
        speed
    ) {

        this.id = id;
        this.name = name;
        this.sprite = image;
        this.height = height;
        this.weight = weight;
        this.hp = hp;
        this.atk = atk;
        this.def = def;
        this.spAtk = spAtk;
        this.spDef = spDef;
        this.speed = speed;

    }
}